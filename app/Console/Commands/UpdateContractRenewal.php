                $child = $contracts->get($renewal);

                if (!$child) {
                    continue;
                }

                $child->update([
                    'parent_contract_id' => $parentId,
                    'renewal_count'      => $renewalCount,
                    'renewal_date'       => date('Y-m-d', strtotime($renewalDate)),
                    'renewed_by'         => 1,
                ]);

